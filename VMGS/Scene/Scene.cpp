	//-------------------------------------------------------------------------
	// ● 按下键
	//-------------------------------------------------------------------------
	void Scene::event_keydown(int key, int mods) {
		switch (key) {
		case GLFW_KEY_PAUSE:
			if (mods & GLFW_MOD_CONTROL) abort();
			break;
		case GLFW_KEY_F3:
			SceneManager::debug_info_visible = !SceneManager::debug_info_visible;
			break;
		}
	}
