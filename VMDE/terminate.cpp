#include "VMDE.hpp"

//-----------------------------------------------------------------------------
// ● 结束
//-----------------------------------------------------------------------------
void terminate_engine() {
	delete VMDE;
	glfwTerminate(); // Terminate GLFW, clearing any resources allocated by GLFW.
	Audio::terminate();
	Util::terminate();
	log("engine terminated");
}
