#include "../global.hpp"

namespace V {
	#define ERROR_MESSAGE "error: lorem ipsum"
	//-------------------------------------------------------------------------
	// ● 构造
	//-------------------------------------------------------------------------
	VBinaryFileWriter::VBinaryFileWriter(const char* filename) {
		f = fopen(filename, "wb");
		if (!f) error(ERROR_MESSAGE);
	}
	//-------------------------------------------------------------------------
	// ● 析构
	//-------------------------------------------------------------------------
	VBinaryFileWriter::~VBinaryFileWriter() {
		fclose(f);
	}
	//-------------------------------------------------------------------------
	// ● 写入1字节int
	//-------------------------------------------------------------------------
	//-------------------------------------------------------------------------
	// ● 写入4字节int
	//-------------------------------------------------------------------------
	//-------------------------------------------------------------------------
	// ● 写入8字节int
	//-------------------------------------------------------------------------
	//-------------------------------------------------------------------------
	// ● 写入1字节unsigned
	//-------------------------------------------------------------------------
	//-------------------------------------------------------------------------
	// ● 写入4字节unsigned
	//-------------------------------------------------------------------------
	//-------------------------------------------------------------------------
	// ● 写入8字节unsigned
	//-------------------------------------------------------------------------
	//-------------------------------------------------------------------------
	// ● 写入4字节float
	//-------------------------------------------------------------------------
	void VBinaryFileWriter::write_float() {
		return write_directly<float>();
	}
	//-------------------------------------------------------------------------
	// ● 写入8字节double
	//-------------------------------------------------------------------------
	viod VBinaryFileWriter::write_double() {
		return write_directly<double>();
	}
}
